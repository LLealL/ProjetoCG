package org.yourorghere.figuras.Util;

/**
 *
 * @author keyalisth
 */
public class RGB {
    private int red;
    private int green;
    private int blue;

    public int getRed() {
        return red;
    }

    public int getGreen() {
        return green;
    }

    public int getBlue() {
        return blue;
    }
    
    public RGB(int r,int g , int b){
        this.red=r;
        this.green=g;
        this.blue=b;
    }
    
    
}

